# Function to handle user signup
def signup():
    signup_window = tk.Toplevel(login_window)
    signup_window.title("Signup Page")
    signup_window.geometry("300x250")

    def register_user():
        new_username = new_username_entry.get()
        new_password = new_password_entry.get()

        if not new_username or not new_password:
            messagebox.showerror("Error", "Username and password cannot be empty!")
            return

        conn = sqlite3.connect("users.db")
        cursor = conn.cursor()

        try:
            cursor.execute(
                "INSERT INTO users (username, password) VALUES (?, ?)",
                (new_username, new_password),
            )
            conn.commit()
            messagebox.showinfo("Success", "User registered successfully!")
            signup_window.destroy()  # Close signup window after successful registration
        except sqlite3.IntegrityError:
            messagebox.showerror("Error", "Username already exists!")

        conn.close()

    tk.Label(signup_window, text="Create a new account", font=("Helvetica", 14)).pack(
        pady=10
    )
    tk.Label(signup_window, text="Username").pack(pady=5)
    new_username_entry = tk.Entry(signup_window)
    new_username_entry.pack(pady=5)

    tk.Label(signup_window, text="Password").pack(pady=5)
    new_password_entry = tk.Entry(signup_window, show="*")
    new_password_entry.pack(pady=5)

    register_button = tk.Button(signup_window, text="Register", command=register_user)
    register_button.pack(pady=20)


